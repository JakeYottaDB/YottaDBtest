	S VCORR="%ABZWQ" D EXAMINER
	;
%ABCDE	S ITEM="I-603.3  %ABCDE" S VCOMP="%ABCDE"
	S VCORR="%ABCDE" D EXAMINER
	;
604	W !,"I-604  label is ""%"" and digit"
	S ITEM="I-604.1  %01"
%01	S VCOMP="%01"
	S VCORR="%01" D EXAMINER
	;
	S ITEM="I-604.2  %000000"
%000000	S VCOMP=" %000000"
	S VCORR=" %000000" D EXAMINER
	;
605	W !,"I-605  label is ""%"" and combination of alpha and digit"
