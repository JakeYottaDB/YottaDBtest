 W !!,"10---V4SORT10: Sort After operator  -10-"
 ;
1 S ^ABSN="40076",^ITEM="IV-76  A is a lvn"
 S ^NEXT="2^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S A(1)="AAAAAAAAAAAAA"
 S ^VCOMP=A(1)]]"AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA"
 S ^VCORR="0" D ^VEXAMINE
 ;
2 S ^ABSN="40077",^ITEM="IV-77  B is a lvn"
 S ^NEXT="3^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S B="BBBBBBBBBBBBBBBBBBBBBBBB"
 S ^VCOMP="BBBBBBBBBBBBBBBBBBBBBBBBB"]]B
 S ^VCORR="1" D ^VEXAMINE
 ;
3 S ^ABSN="40078",^ITEM="IV-78  A is a gvn"
 S ^NEXT="4^V4SORT10,V4FNUM^VV4" D ^V4PRESET K  K ^VV
 S ^VV(1)="985.56"
 S ^VCOMP=^VV(1)]]-1
 S ^VCORR="1" D ^VEXAMINE K ^VV
 ;
4 S ^ABSN="40079",^ITEM="IV-79  B is a gvn"
 S ^NEXT="5^V4SORT10,V4FNUM^VV4" D ^V4PRESET K  K ^VV
 S ^VV(00)=-1E-10
 S ^VCOMP=0]]^VV(0)
 S ^VCORR="1" D ^VEXAMINE
 ;
5 S ^ABSN="40080",^ITEM="IV-80  A is a function"
 S ^NEXT="6^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S ^VCOMP=$J(1234567890E-5,0,2)]]1234
 S ^VCORR="1" D ^VEXAMINE
 ;
6 S ^ABSN="40081",^ITEM="IV-81  B is a function"
 S ^NEXT="7^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S ^VCOMP="ABC"]]$P("AB/CD/EF/GH","/",3)
 S ^VCORR="0" D ^VEXAMINE
 ;
7 S ^ABSN="40082",^ITEM="IV-82  A contains unary operator"
 S ^NEXT="8^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S ^VCOMP='0]]0
 S ^VCORR="1" D ^VEXAMINE
 ;
8 S ^ABSN="40083",^ITEM="IV-83  B contains unary operator"
 S ^NEXT="9^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S ^VCOMP=1]]+"-123.00"
 S ^VCORR="1" D ^VEXAMINE
 ;
9 S ^ABSN="40084",^ITEM="IV-84  A contains indirections"
 S ^NEXT="10^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S A="B(1)",B(1,2,3)="ABCD"
 S ^VCOMP=@A@(2,3)]]"abcd"
 S ^VCORR="0" D ^VEXAMINE
 ;
10 S ^ABSN="40085",^ITEM="IV-85  B contains indirections"
 S ^NEXT="11^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S B(1)="@B",B="C",C="D"
 S ^VCOMP="a"]]@B(1)
 S ^VCORR="1" D ^VEXAMINE
 ;
11 S ^ABSN="40086",^ITEM="IV-86  A is an expr"
 S ^NEXT="12^V4SORT10,V4FNUM^VV4" D ^V4PRESET K
 S ^VCOMP=("ABC"_"DEF"_"")]]"ABCDEF"
 S ^VCORR="0" D ^VEXAMINE
 ;
12 S ^ABSN="40087",^ITEM="IV-87  B is an expr"
 S ^NEXT="V4FNUM^VV4" D ^V4PRESET K
 S A="123456788"
 S ^VCOMP="{"]]($L(A)*3)
 S ^VCORR="1" D ^VEXAMINE
 ;
END W !!,"End of 10 --- V4SORT10",!
 K  Q
 ;
SUM S SUM=0 F I=1:1 S L=$T(+I) Q:L=""  F K=1:1:$L(L) S SUM=SUM+$A(L,K)
 Q
