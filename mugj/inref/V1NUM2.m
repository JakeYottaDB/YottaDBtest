	S ITEM="I-665.9  -00000.0000000" S VCOMP=-00000.0000000,VCORR=0 D EXAMINER
	;
666	W !,"I-666  abbreviation of multiple minus sign (-)"
	S ITEM="I-666.1  -432" S VCOMP=-432,VCORR=-432 D EXAMINER
	S ITEM="I-666.2  --06.54" S VCOMP=--06.54,VCORR=6.54 D EXAMINER
