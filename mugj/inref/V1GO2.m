V1GO2	;GOTO COMMAND (LOCAL BRANCHING) -2-;KO-TS,V1GO,VALIDATION VERSION 7.1;31-AUG-1987;
	;COPYRIGHT MUMPS SYSTEM LABORATORY 1978
	S PASS=0,FAIL=0
	W !!,"V1GO2: TEST OF GOTO COMMAND (LOCAL BRANCHING) -2-",!
	W !,"GOTO label+intexpr",!
	S VCORR="0000000 A9Z00 02 " D 385 D EXAMINER
	S VCORR="ZERO ZERO2" D 386 D EXAMINER
	S VCORR="%000 NOTES STAR1 00000000 " D 387 D EXAMINER
	S VCORR="ANSI+1 388+3" D 388 D EXAMINER
	S VCORR="STAR3 R " D 389 D EXAMINER
	S VCORR="%0+1 %00 99999999 ANSI " D 390 D EXAMINER
	S VCORR="%0 " D 391 D EXAMINER
	S VCORR="HAL9000+1 " D 392 D EXAMINER
	S VCORR="%ABC8 " D 827 D EXAMINER
	S VCORR="STAR " D 828 D EXAMINER
END	W !!,"END OF V1GO2",!
	S ROUTINE="V1GO2",TESTS=10,AUTO=10,VISUAL=0 D ^VREPORT
	K  K ^V1A Q
	;
385	W !,"I-385  intexpr is positive integer"
	S ITEM="I-385  ",VCOMP=""
	G 00000000+2
02	S VCOMP=VCOMP_"02 "
	Q
	;
386	W !,"I-386  intexpr is zero"
	S ITEM="I-386  ",VCOMP=""
	G %Z0+0 S VCOMP=VCOMP_"E-3862 "
%Z00	S VCOMP=VCOMP_"ZERO2"
	Q
	;
387	W !,"I-387  intexpr is non-integer numeric literal"
	S ITEM="I-387  ",VCOMP=""
	G 0000000+7.999
	;
388	W !,"I-388  intexpr is function"
	S ITEM="I-388  ",VCOMP="" K A
	S A(1,1)=11 G %000+$D(A(1))-$L(0.20+0.000450)
	S VCOMP=VCOMP_"388+3"
	Q
	;
389	W !,"I-389  intexpr is gvn"
	S ITEM="I-389  ",VCOMP=""
	S ^V1A=3 G STAR+^V1A
	S VCOMP=VCOMP_"++"
	S VCOMP=VCOMP_"R " Q
	;
390	W !,"I-390  intexpr contains binary operator"
	S ITEM="I-390  ",VCOMP="" K A S A=2
	G %0+A=2
	;
391	W !,"I-391  intexpr contains unary operator"
	S ITEM="I-391  ",VCOMP=""
	G %0+-+-+'-'0
	;
392	W !,"I-392  intexpr contains gvn as expratom"
	S ITEM="I-392  ",VCOMP="" K ^V1A
	S ^V1A(2)=20,ZORAC=19 G HAL9000+^V1A(2)-ZORAC
	;
827	W !,"I-827  argument list label without postcondition"
	S ITEM="I-827  ",VCOMP=""
	G %ABC8,ZZZZZZZZ,Z897
	;
828	W !,"I-828  argument list label+intexpr without postcondition"
	S ITEM="I-828  ",VCOMP=""
	G %Z0+01,ANSI+2,0000000+05
	;
%ABC8	S VCOMP=VCOMP_"%ABC8 " Q
HAL9000	S VCOMP=VCOMP_"HAL9000 " Q
	S VCOMP=VCOMP_"HAL9000+1 " Q
ZZZZZZZZ	S VCOMP=VCOMP_"ZZZZZZZZ " Q
Z897	S VCOMP=VCOMP_"Z897 " Q
00000000	S VCOMP=VCOMP_"00000000 " Q  ;387
ABCDEF	S VCOMP=VCOMP_"ABCDEF " Q  ;835
0000000	S VCOMP=VCOMP_"0000000 " G ABCDEF+000002 ;385
A9Z00	S VCOMP=VCOMP_"A9Z00 " GOTO 385+3000E-3 ;385
%Z0	S VCOMP=VCOMP_"ZERO " G %Z00+0000 ;386
STAR	S VCOMP=VCOMP_"STAR " Q  ;828
	S VCOMP=VCOMP_"STAR1 " G ZZZZZZZZ+"2A" ;387
	S VCOMP=VCOMP_"STAR2 " Q  ;828
	S VCOMP=VCOMP_"STAR3 ",A(3)="-1" GOTO 389+^V1A-A(^V1A) ;389
%000	S VCOMP=VCOMP_"%000 " G %Z0++"+.7E+1" ;387
99999999	S VCOMP=VCOMP_"99999999 " G A9Z00+-91+100 ;390
NOTES	S VCOMP=VCOMP_"NOTES " G STAR++.1E+1 ;387
ANSI	S VCOMP=VCOMP_"ANSI " Q  ;390
	S VCOMP=VCOMP_"ANSI+1 " G 388+$L($E(VCOMP,5,99)) ;388
	S VCOMP=VCOMP_"ANSI+2 " Q  ;828
%00	S VCOMP=VCOMP_"%00 " G 99999999+1+1/2-1 ;390
%0	S VCOMP=VCOMP_"%0 " Q  ;391
	S VCOMP=VCOMP_"%0+1 " ;390
	S C=12.3,D="6A",E="5ABCDEFG" G NOTES+C+(D*2)+E=29.3+3
	;
EXAMINER	I VCORR=VCOMP S PASS=PASS+1 W !,"   PASS  ",ITEM W:$Y>55 # Q
	S FAIL=FAIL+1 W !,"** FAIL  ",ITEM W:$Y>55 #
	W !,"           COMPUTED =""",VCOMP,"""" W:$Y>55 #
	W !,"           CORRECT  =""",VCORR,"""" W:$Y>55 #
	Q
