	S ITEM="I-800.1.7  'mantE+intlit",VCOMP='2.2E+2 S VCORR="0" D EXAMINER
	S ITEM="I-800.1.8  'mantE-intlit",VCOMP='2.2E-2 S VCORR="0" D EXAMINER
	;
8002	W !,"I-800.2  not unary operator and a strlit"
	S ITEM="I-800.2.1  '""0""",VCOMP='"0",VCORR="1" D EXAMINER
	S ITEM="I-800.2.2  '""1""",VCOMP='"1",VCORR="0" D EXAMINER
	S ITEM="I-800.2.3  'intlit",VCOMP='"2BCDEAS43",VCORR="0" D EXAMINER
