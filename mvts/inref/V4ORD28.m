 S A(B,C,D)="X"
 S A(B,C,D,12345.678)=1234
 S ^VCOMP=$o(A(B,C,D,A),E("e"))
 S ^VCORR="-12345.678" D ^VEXAMINE
 ;
6 S ^ABSN="40728",^ITEM="IV-728  glvn is a gvn"
 S ^NEXT="7^V4ORD28,V4ORD29^V4ORDER,V4QUERY^VV4" D ^V4PRESET K  K ^V
