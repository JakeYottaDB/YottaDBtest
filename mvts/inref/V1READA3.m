 W !!,"184---V1READA3: READ command -3-",!
TEST2 W !!,"READ *lvn :",!
757 W !,"I-757  READ *lvn"
 S ^ABSN="12060",^ITEM="I-757.1  A",^NEXT="7572^V1READA3,V1READB^VV1" D ^V1PRESET
 I $D(^VENVIRON("INPUT USE"))=1 U ^VENVIRON("INPUT USE")
 R !!,"TEST I-757.1: Type 1 character 'A' : ",*%
 S ^VCOMP=%
 I $D(^VENVIRON("OUTPUT USE"))=1 U ^VENVIRON("OUTPUT USE")
 S ^VCORR="65"
 D AGAIN^VEXAMINE I RES="YES" G 757
 D ^VEXAMINE
 ;
7572 S ^ABSN="12061",^ITEM="I-757.2  <CR>",^NEXT="758^V1READA3,V1READB^VV1" D ^V1PRESET
 I $D(^VENVIRON("INPUT USE"))=1 U ^VENVIRON("INPUT USE")
 READ !!,"TEST I-757.2: Type only a <CR>  : ",*%2345678
 S ^VCOMP=%2345678
 I $D(^VENVIRON("OUTPUT USE"))=1 U ^VENVIRON("OUTPUT USE")
 S ^VCORR="13"
 D AGAIN^VEXAMINE I RES="YES" G 7572
 D ^VEXAMINE
 ;
758 W !!,"I-758  READ *lvn,*lvn,*lvn"
 S ^ABSN="12062",^ITEM="I-758  READ *lvn,*lvn,*lvn",^NEXT="759^V1READA3,V1READB^VV1" D ^V1PRESET
 S VCOMP=""
 I $D(^VENVIRON("INPUT USE"))=1 U ^VENVIRON("INPUT USE")
 R !!,"TEST I-758: Type 3 characters  '1!Z'  in this sequence : ",*ABCDEFGH,*%234567A,*Z
 S ^VCOMP=ABCDEFGH_" "_%234567A_" "_Z
 I $D(^VENVIRON("OUTPUT USE"))=1 U ^VENVIRON("OUTPUT USE")
 S ^VCORR="49 33 90"
 D AGAIN^VEXAMINE I RES="YES" G 758
 D ^VEXAMINE
 ;
759 W !!,"I-759  Read into subscripted variable"
7591 S ^ABSN="12063",^ITEM="I-759.1  READ lvn",^NEXT="7592^V1READA3,V1READB^VV1" D ^V1PRESET
 K MUMPS
 I $D(^VENVIRON("INPUT USE"))=1 U ^VENVIRON("INPUT USE")
 R !!,"TEST I-759.1: Type 5 characters 'MUMPS' and a <CR> : ",!,MUMPS(100) S ^VCOMP=MUMPS(100)
 I $D(^VENVIRON("OUTPUT USE"))=1 U ^VENVIRON("OUTPUT USE")
 S ^VCORR="MUMPS"
 D AGAIN^VEXAMINE I RES="YES" G 759
 D ^VEXAMINE
 ;
7592 S ^ABSN="12064",^ITEM="I-759.2  READ *lvn",^NEXT="V1READB^VV1" D ^V1PRESET
 K MUMPS
 I $D(^VENVIRON("INPUT USE"))=1 U ^VENVIRON("INPUT USE")
 R !!,"TEST I-759.2: Type one character 'M'  : ",!,*MUMPS(100,1) S ^VCOMP=MUMPS(100,1)
 I $D(^VENVIRON("OUTPUT USE"))=1 U ^VENVIRON("OUTPUT USE")
 S ^VCORR=77
 D AGAIN^VEXAMINE I RES="YES" G 7592
 D ^VEXAMINE
 ;
END W !!,"End of 184---V1READA3",!
 K  Q
SUM S SUM=0 F I=1:1 S L=$T(+I) Q:L=""  F K=1:1:$L(L) S SUM=SUM+$A(L,K)
 Q
