	S VCORR="%09AB" D EXAMINER
	;
%ABC000	S ITEM="I-605.3  %ABC000" S VCOMP="%ABC000"
	S VCORR="%ABC000" D EXAMINER
	;
%ABC0000	S ITEM="I-605.4  %ABC0000" S VCOMP="%ABC0000"
	S VCORR="%ABC0000" D EXAMINER
	;
