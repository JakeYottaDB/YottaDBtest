 W !!,"7---V3TR01: $TRANSLATE function -1-"
 W !!,"$TRANSLATE(expr1,expr2)"
 ;
 W !!,"III-87  expr1 and expr2 are strlits",!
 ;
1 S ^ABSN="30087",^ITEM="III-87  expr1 and expr2 are strlits"
 S ^NEXT="2^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TRANSLATE("ABCabcABCabc","Ab")
 S ^VCORR="BCacBCac" D ^VEXAMINE
 ;
 W !!,"expr1 undergoes no change",!
 ;
2 S ^ABSN="30088",^ITEM="III-88  expr1 is an empty string"
 S ^NEXT="3^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$translate("","ABCDEFGHIJ")
 S ^VCORR="" D ^VEXAMINE
 ;
3 S ^ABSN="30089",^ITEM="III-89  expr2 is an empty string"
 S ^NEXT="4^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABCDEFGHIJ","")
 S ^VCORR="ABCDEFGHIJ" D ^VEXAMINE
 ;
4 S ^ABSN="30090",^ITEM="III-90  Both expr1 and expr2 are empty strings"
 S ^NEXT="5^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$tr("","")
 S ^VCORR="" D ^VEXAMINE
 ;
5 S ^ABSN="30091",^ITEM="III-91  expr1 has no chars of expr2"
 S ^NEXT="6^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABCDE","abcde")
 S ^VCORR="ABCDE" D ^VEXAMINE
 ;
 W !!,"expr2 has 1 char",!
 ;
6 S ^ABSN="30092",^ITEM="III-92  The heading char is removed"
 S ^NEXT="7^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABCDE","A")
 S ^VCORR="BCDE" D ^VEXAMINE
 ;
7 S ^ABSN="30093",^ITEM="III-93  The heading substring is removed"
 S ^NEXT="8^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("AABBCC","A")
 S ^VCORR="BBCC" D ^VEXAMINE
 ;
8 S ^ABSN="30094",^ITEM="III-94  A middle char is removed"
 S ^NEXT="9^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABCDE","C")
 S ^VCORR="ABDE" D ^VEXAMINE
 ;
9 S ^ABSN="30095",^ITEM="III-95  A middle substring is removed"
 S ^NEXT="10^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("AABBCC","B")
 S ^VCORR="AACC" D ^VEXAMINE
 ;
10 S ^ABSN="30096",^ITEM="III-96  The last char is removed"
 S ^NEXT="11^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABCDE","E")
 S ^VCORR="ABCD" D ^VEXAMINE
 ;
11 S ^ABSN="30097",^ITEM="III-97  The tailing substring is removed"
 S ^NEXT="12^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("AABBCC","C")
 S ^VCORR="AABB" D ^VEXAMINE
 ;
12 S ^ABSN="30098",^ITEM="III-98  All chars are removed"
 S ^NEXT="13^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("AAAAA","A")
 S ^VCORR="" D ^VEXAMINE
 ;
 W !!,"expr2 has 2 chars",!
 ;
13 S ^ABSN="30099",^ITEM="III-99  The heading 2 chars are removed"
 S ^NEXT="14^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABCDEFGHIJ","AB")
 S ^VCORR="CDEFGHIJ" D ^VEXAMINE
 ;
14 S ^ABSN="30100",^ITEM="III-100  The heading substring is removed"
 S ^NEXT="15^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABBACDDC","AB")
 S ^VCORR="CDDC" D ^VEXAMINE
 ;
15 S ^ABSN="30101",^ITEM="III-101  A middle substring is removed"
 S ^NEXT="16^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABBACDDCEFFE","CD")
 S ^VCORR="ABBAEFFE" D ^VEXAMINE
 ;
16 S ^ABSN="30102",^ITEM="III-102  The tailing substring is removed"
 S ^NEXT="17^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABBACDDCEFFE","EF")
 S ^VCORR="ABBACDDC" D ^VEXAMINE
 ;
17 S ^ABSN="30103",^ITEM="III-103  The middle and tailing substrings are removed"
 S ^NEXT="18^V3TR01,V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABBACDDCABBACDDC","CD")
 S ^VCORR="ABBAABBA" D ^VEXAMINE
 ;
18 S ^ABSN="30104",^ITEM="III-104  All chars are removed"
 S ^NEXT="V3TR02^V3TR,V3TEXT^VV3" D ^V3PRESET
 S ^VCOMP=$TR("ABBAABBA","AB")
 S ^VCORR="" D ^VEXAMINE
 ;
END W !!,"End of 7 --- V3TR01",!
 K  Q
 ;
SUM S SUM=0 F I=1:1 S L=$T(+I) Q:L=""  F K=1:1:$L(L) S SUM=SUM+$A(L,K)
 Q
