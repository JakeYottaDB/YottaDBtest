V1RN	;ACCEPTABLE ROUTINE NAME;YS-TS,V1RN,VALIDATION VERSION 7.1;31-AUG-1987;
	;COPYRIGHT MUMPS SYSTEM LABORATORY 1978
	S PASS=0,FAIL=0
	W !!,"V1RN: TESTS ACCEPTABLE ROUTINE NAMES",!
776	W !,"I-776  routinename is ""%"""
	S ITEM="I-776  "
	S VCOMP="" D ^%
	S VCORR="% " D EXAMINER
	;
777	W !,"I-777  routinename is ""%"" followed by alpha and digit"
	S ITEM="I-777  ",VCOMP=""
	DO ^%1A
	S VCORR="%1A " D EXAMINER
	;
778	W !,"I-778  routinename is alphas"
	S ITEM="I-778  ",VCOMP=""
	D ^V
	D ^VA DO ^VAB
	D ^VABC D ^VABCD D ^VABCDE DO ^VABCDEF ;COMMENT
	S VCORR="V VA VAB VABC VABCD VABCDE VABCDEF " D EXAMINER
	;
779	W !,"I-779  routinename is alpha followed by digits"
	S ITEM="I-779  ",VCOMP=""
	D ^V0
	D ^V01,^V012
	DO ^V4444,^V12345,^V000006
	SET VCORR="V0 V01 V012 V4444 V12345 V000006 " DO EXAMINER
	;
780	W !,"I-780  Maximum length of routinename"
	S ITEM="I-780  ",VCOMP=""
	D ^VABCDEFG
	D ^VABCDEFH
	D ^V7777777,^%2345678 D ^%BCDEFGH
	S VCORR="VABCDEFG VABCDEFH V7777777 %2345678 %BCDEFGH " D EXAMINER
	;
END	W !!,"END OF V1RN",!
	S ROUTINE="V1RN",TESTS=5,AUTO=5,VISUAL=0 D ^VREPORT
	QUIT
	;
EXAMINER	I VCORR=VCOMP S PASS=PASS+1 W !,"   PASS  ",ITEM W:$Y>55 # Q
	S FAIL=FAIL+1 W !,"** FAIL  ",ITEM W:$Y>55 #
	W !,"           COMPUTED =""",VCOMP,"""" W:$Y>55 #
	W !,"           CORRECT  =""",VCORR,"""" W:$Y>55 #
	Q
