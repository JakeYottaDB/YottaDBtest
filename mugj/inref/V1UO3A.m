	S ITEM="I-800.2.10  '""AB2""",VCOMP='"AB2",VCORR="1" D EXAMINER
	S ITEM="I-800.2.11  '""2A2B""",VCOMP='"2A2B",VCORR="0" D EXAMINER
	;
