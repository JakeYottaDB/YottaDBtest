 W !!,"10---V1PRIE: Preliminary tests of IF and ELSE commands",!
731 W !,"I-731/733  Interpretation of ifargument and ELSE command"
7311 S ^ABSN="10062",^ITEM="I-731/733.1  ifargument is 0 ",^NEXT="7312^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 IF 0 S VCOMP=VCOMP_"/1" ELSE  S VCOMP=VCOMP_"/2"
 ELSE  S VCOMP=VCOMP_" OK"
 S ^VCOMP=VCOMP,^VCORR=" OK" D ^VEXAMINE
 ;
7312 S ^ABSN="10063",^ITEM="I-731/733.2  ifargument is 1 ",^NEXT="7313^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I 1 S VCOMP=VCOMP_"/A" E  S VCOMP=VCOMP_"/3"
 E  S VCOMP=VCOMP_"/4"
 S ^VCOMP=VCOMP,^VCORR="/A" D ^VEXAMINE
 ;
7313 S ^ABSN="10064",^ITEM="I-731/733.3  ifargument is 2 ",^NEXT="7314^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I 2 S VCOMP=VCOMP_"/B"
 E  S VCOMP=VCOMP_"/5"
 S ^VCOMP=VCOMP,^VCORR="/B" D ^VEXAMINE
 ;
7314 S ^ABSN="10065",^ITEM="I-731/733.4  ifargument is -1",^NEXT="7315^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I -1 S VCOMP=VCOMP_"/C"
 E  S VCOMP=VCOMP_"/6"
 S ^VCOMP=VCOMP,^VCORR="/C" D ^VEXAMINE
 ;
7315 S ^ABSN="10066",^ITEM="I-731/733.5  ifargument is -0.00000001",^NEXT="7316^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I -0.00000001 S VCOMP=VCOMP_"/D"
 E  S VCOMP=VCOMP_"/7"
 S ^VCOMP=VCOMP,^VCORR="/D" D ^VEXAMINE
 ;
7316 S ^ABSN="10067",^ITEM="I-731/733.6  List of IF command and all ifargument is true",^NEXT="7317^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I 1 I 2 I 3 I 4 S VCOMP=VCOMP_"/E"
 E  S VCOMP=VCOMP_"/8"
 S ^VCOMP=VCOMP,^VCORR="/E" D ^VEXAMINE
 ;
7317 S ^ABSN="10068",^ITEM="I-731/733.7  List of IF command and a ifargument is false",^NEXT="732^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I 1 I 2 I 0 I 4 S VCOMP=VCOMP_"/9"
 E  S VCOMP=VCOMP_"/F"
 S ^VCOMP=VCOMP,^VCORR="/F" D ^VEXAMINE
 ;
732 W !,"I-732/733  Argument list of IF command and ELSE command"
7321 S ^ABSN="10069",^ITEM="I-732/733.1  All ifargument is true",^NEXT="7322^V1PRIE,V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I 1,2,3,-4,5 S VCOMP=VCOMP_"/G"
 E  S VCOMP=VCOMP_"/10"
 S ^VCOMP=VCOMP,^VCORR="/G" D ^VEXAMINE
 ;
7322 S ^ABSN="10070",^ITEM="I-732/733.2  A ifargument is false",^NEXT="V1PRFOR^VV1" D ^V1PRESET
 S VCOMP=""
 I 1,-2,3,0,4 S VCOMP=VCOMP_"/11"
 E  S VCOMP=VCOMP_"/H"
 S ^VCOMP=VCOMP,^VCORR="/H" D ^VEXAMINE
 ;
END W !!,"End of 10---V1PRIE",!
 K  Q
SUM S SUM=0 F I=1:1 S L=$T(+I) Q:L=""  F K=1:1:$L(L) S SUM=SUM+$A(L,K)
 Q
