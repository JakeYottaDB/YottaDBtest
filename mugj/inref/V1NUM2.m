	S ITEM="I-666.7  +---000006.54E+0002" S VCOMP=+---000006.54E+0002,VCORR=-654 D EXAMINER
	S ITEM="I-666.8  +---+00006.54E-0003" S VCOMP=+---+00006.54E-0003,VCORR=-.00654 D EXAMINER
	S ITEM="I-666.9  +6.540000000000000E-3" S VCOMP=+6.540000000000000E-3,VCORR=.00654 D EXAMINER
