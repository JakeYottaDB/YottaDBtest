 S ^V("A",2,1)=21,^V("A",2,"ZZZZZZ")="zzz"
 S ^V("A","B")="AB"
 S ^VCOMP=$o(^(2,3),1)_" "_^(1)
 S ^VCORR="ZZZZZZ 21" D ^VEXAMINE K ^V
 ;
5 S ^ABSN="40727",^ITEM="IV-727  glvn is a lvn"
 S ^NEXT="6^V4ORD28,V4ORD29^V4ORDER,V4QUERY^VV4" D ^V4PRESET K
 S A=-1
