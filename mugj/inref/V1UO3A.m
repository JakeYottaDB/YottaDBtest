8001	W !,"I-800.1  not unary operator and a numlit"
	S ITEM="I-800.1.1  '0",VCOMP='0 S VCORR="1" D EXAMINER
	S ITEM="I-800.1.2  '1",VCOMP='1 S VCORR="0" D EXAMINER
