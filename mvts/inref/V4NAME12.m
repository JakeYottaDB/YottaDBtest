 W !!,"33---V4NAME12:  $NAME function  -2-"
 ;
 W !,"2 subscripts"
 ;
1 S ^ABSN="40249",^ITEM="IV-249  subscript is an integer number"
 S ^NEXT="2^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S ^VCOMP=$NA(A(-0,-99.99E+15))
 S ^VCORR="A(0,-99990000000000000)" D ^VEXAMINE
 ;
2 S ^ABSN="40250",^ITEM="IV-250  subscript is a number"
 S ^NEXT="3^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S ^VCOMP=$name(V(0.000000000000040000,00400034.56000000E+3))
 S ^VCORR="V(.00000000000004,400034560)" D ^VEXAMINE
 ;
3 S ^ABSN="40251",^ITEM="IV-251  subscript are numbers"
 S ^NEXT="4^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S ^VCOMP=$NAME(V1234567(-987E-20,"-5678.456"))
 S ^VCORR="V1234567(-.00000000000000000987,-5678.456)" D ^VEXAMINE
 ;
4 S ^ABSN="40252",^ITEM="IV-252  subscript is a string"
 S ^NEXT="5^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S ^VCOMP=$na(A("-10.","456.0001"))
 S ^VCORR="A(""-10."",456.0001)" D ^VEXAMINE
 ;
5 S ^ABSN="40253",^ITEM="IV-253  subscript are strings"
 S ^NEXT="6^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S A="1234.56700000",B("A")="ba"
 S ^VCOMP=$na(VV(A,B("A")))
 S ^VCORR="VV(""1234.56700000"",""ba"")" D ^VEXAMINE
 ;
6 S ^ABSN="40254",^ITEM="IV-254  subscript contains a "" character"
 S ^NEXT="7^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S S=$C(34),S(1,2,3)="1E10"
 S ^VCOMP=$NAME(V(S,S(1,2,3)))
 S ^VCORR="V("""""""",""1E10"")" D ^VEXAMINE
 ;
7 S ^ABSN="40255",^ITEM="IV-255  subscript contains "" characters"
 S ^NEXT="8^V4NAME12,V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S S=$C(34),SS=S_S_S_S
 S V(S,SS)=2
 S ^VCOMP=$NA(V(S,SS))
 S ^VCORR="V("""""""","""""""""""""""""""")" D ^VEXAMINE
 ;
8 S ^ABSN="40256",^ITEM="IV-256  5 subscripts"
 S ^NEXT="V4NAME13^V4NAME,V4QLEN^VV4" D ^V4PRESET K
 S A="000000000567.430E1"
 S ^VCOMP=$NA(Z(+A,-A,'A,A_A,A?5."0"1.N1"."1.N1"E".E))
 S ^VCORR="Z(5674.3,-5674.3,0,""000000000567.430E1000000000567.430E1"",1)" D ^VEXAMINE
 ;
 ;
END W !!,"End of 33 --- V4NAME12",!
 K  Q
 ;
SUM S SUM=0 F I=1:1 S L=$T(+I) Q:L=""  F K=1:1:$L(L) S SUM=SUM+$A(L,K)
 Q
