V1GVN	;ACCEPTABLE GLOBAL VARIABLE NAME;YS-TS,,VALIDATION VERSION 7.1;31-AUG-1987;
	;COPYRIGHT MUMPS SYSTEM LABORATORY 1978
	S PASS=0,FAIL=0
	W !!,"V1GVN: TEST OF ACCEPTABLE GLOBAL VARIABLE NAMES",!
393	W !,"I-393  gvn is ""%"""
	S ITEM="I-393  "
	S VCOMP="",^%="^%" S VCOMP=^% ;SYSTEM ERROR MAY RESULT
	S VCORR="^%" D EXAMINER
	;
394	W !,"I-394  gvn is % followed by alphas"
	S ITEM="I-394  "
	S VCOMP="",^%ABCDEF="^%ABCDEF" S VCOMP=^%ABCDEF
	S VCORR="^%ABCDEF" D EXAMINER
	;
395	W !,"I-395  gvn is % followed by digits"
	S ITEM="I-395  "
	S VCOMP="",^%1234="^%1234" S VCOMP=^%1234
	S VCORR="^%1234" D EXAMINER
	;
396	W !,"I-396  gvn is % followed by alpha and digit"
	S ITEM="I-396.1  gvn is % followed by a alpha and a digit"
	S VCOMP="",^%A1="^%A1" S VCOMP=^%A1
	S VCORR="^%A1" D EXAMINER
	;
	S ITEM="I-396.2  gvn is % followed by alphas and digits"
	S VCOMP="",^%ABC456GH="^%ABC456GH",VCOMP=^%ABC456GH
	S VCORR="^%ABC456GH" D EXAMINER
	;
397	W !,"I-397  gvn is a alpha"
	S ITEM="I-397  ^V" S VCOMP="",^V="^V",VCOMP=^V,VCORR="^V" D EXAMINER
	;
398	W !,"I-398  gvn is alpha and digit"
	S ITEM="I-398.1  ^V1",VCOMP="",^V1="^V1",VCOMP=^V1,VCORR="^V1" D EXAMINER
	S ITEM="I-398.2  ^V1A",VCOMP="",^V1A="^V1A",VCOMP=^V1A,VCORR="^V1A" D EXAMINER
	S ITEM="I-398.3  ^V100",VCOMP="",^V100="^V100",VCOMP=^V100,VCORR="^V100" D EXAMINER
	S ITEM="I-398.4  ^V1AB",VCOMP="",^V1AB="^V1AB",VCOMP=^V1AB,VCORR="^V1AB" D EXAMINER
	S ITEM="I-398.5  ^V1Z1Y2X",VCOMP="",^V1Z1Y2X="^V1Z1Y2X",VCOMP=^V1Z1Y2X,VCORR="^V1Z1Y2X" D EXAMINER
	S ITEM="I-398.6  ^V1ABCDE",VCOMP="",^V1ABCDE="^V1ABCDE",VCOMP=^V1ABCDE,VCORR="^V1ABCDE" D EXAMINER
	;
399	W !,"I-399  Maximum length of gvn"
	S ITEM="I-399.1  gvn is ^V1ABCDEF"
	S VCOMP="",^V1ABCDEF="^V1ABCDEF" S VCOMP=^V1ABCDEF
	S VCORR="^V1ABCDEF" D EXAMINER
	;
	S ITEM="I-399.2  ^V100000A"
	S VCOMP="",^V100000A="^V100000A" S VCOMP=^V100000A
	S VCORR="^V100000A" D EXAMINER
	;
400	W !,"I-400  8 levels depth subscript"
	S ITEM="I-400.1  1 level depth subscript"
	S VCOMP="",^V1(1)="^V1(1)" S VCOMP=^V1(1)
	S VCORR="^V1(1)" D EXAMINER
	;
	S ITEM="I-400.2  8 levels depth subscript"
	S VCOMP="",^V1ABCDEF(1,2,3,4,5,6,7,8)="^V1ABCDEF(1,2,3,4,5,6,7,8)" S VCOMP=^V1ABCDEF(1,2,3,4,5,6,7,8)
	S VCORR="^V1ABCDEF(1,2,3,4,5,6,7,8)" D EXAMINER
	;
	S ITEM="I-400.3  gvn is ^%1X2Y3Z(1,1,1,1,1,1,1,1)"
	S VCOMP="",^%1X2Y3Z(1,1,1,1,1,1,1,1)="^%1X2Y3Z(1,1,1,1,1,1,1,1)" S VCOMP=^%1X2Y3Z(1,1,1,1,1,1,1,1)
	S VCORR="^%1X2Y3Z(1,1,1,1,1,1,1,1)" D EXAMINER
	;
END	W !!,"END OF V1GVN",!
	K ^%,^%A1,^%1234,^%ABCDEF,^%ABC456GH,^%1X2Y3Z
	K ^V,^V1,^V1A,^V100,^V1AB,^V1Z1Y2X,^V1ABCDE,^V1ABCDEF,^V100000A
	S ROUTINE="V1GVN",TESTS=17,AUTO=17,VISUAL=0 D ^VREPORT
	K  Q
	;
EXAMINER	I VCORR=VCOMP S PASS=PASS+1 W !,"   PASS  ",ITEM W:$Y>55 # Q
	S FAIL=FAIL+1 W !,"** FAIL  ",ITEM W:$Y>55 #
	W !,"           COMPUTED =""",VCOMP,"""" W:$Y>55 #
	W !,"           CORRECT  =""",VCORR,"""" W:$Y>55 #
	Q
