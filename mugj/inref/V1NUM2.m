	S PASS=0,FAIL=0
	W !!,"V1NUM2: TEST OF NUMERIC LITERALS -2-",!
665	W !,"I-665  deletion of trailing zero, while expr is non-integer"
	S ITEM="I-665.1  1.23" S VCOMP=1.23,VCORR=1.23 D EXAMINER
	S ITEM="I-665.2  456.7890" S VCOMP=456.7890,VCORR=456.789 D EXAMINER
