 D SETL2^V4ORDE S VCOMP="",X=""
 FOR I=1:1 S X=$O(V(X),1) S VCOMP=VCOMP_X_" " Q:X=""
 S ^VCOMP=VCOMP,^VCORR=X(4) D ^VEXAMINE
