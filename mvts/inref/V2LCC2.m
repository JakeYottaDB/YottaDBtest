 W !!,"3---V2LCC2: Lower case command words and $data -2-",!
21 W !,"II-21  if"
 S ^ABSN="20021",^ITEM="II-21  if",^NEXT="22^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP=""
 if .1 SET ^VCOMP=^VCOMP_7 if  S ^VCOMP=^VCOMP_8
 if  S ^VCOMP=^VCOMP_9
 S ^VCOMP=^VCOMP_" "_$TEST if 0.0 S ^VCOMP=^VCOMP_"ERORR if0"
 S ^VCOMP=^VCOMP_" "_$TEST,^VCORR="789 1 0" D ^VEXAMINE
 ;
22 W !,"II-22  i"
 S ^ABSN="20022",^ITEM="II-22  i",^NEXT="23^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP=""
 i .1 SET ^VCOMP=^VCOMP_10 i  S ^VCOMP=^VCOMP_11
 i  S ^VCOMP=^VCOMP_12
 S ^VCOMP=^VCOMP_" "_$TEST i 0.0 S ^VCOMP=^VCOMP_"ERORR i0"
 S ^VCOMP=^VCOMP_" "_$TEST,^VCORR="101112 1 0" D ^VEXAMINE
 ;
23 W !,"II-23  else"
 S ^ABSN="20023",^ITEM="II-23  else",^NEXT="24^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="" if 1 S ^VCOMP=^VCOMP_$TEST_" "
 elSe  S ^VCOMP=^VCOMP_"elSe COMMAND ERROR"
 ELse  S ^VCOMP=^VCOMP_"ELse COMMAND ERROR"
 if 0.000 S ^VCOMP=^VCOMP_"ERROR if0" ;(test corrected in V7.6;1/3/91)
 S ^VCOMP=^VCOMP_$T_" "
 else  S ^VCOMP=^VCOMP_"#"
 ELse  S ^VCOMP=^VCOMP_"$"
 S ^VCOMP=^VCOMP_" "_$T,^VCORR="1 0 #$ 0" D ^VEXAMINE
 ;
24 W !,"II-24  e"
 S ^ABSN="20024",^ITEM="II-24  e",^NEXT="25^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="" i 00010000 S ^VCOMP=^VCOMP_$TEST_" "
 e  S ^VCOMP=^VCOMP_"elSe COMMAND ERROR"
 i 0.0 S ^VCOMP=^VCOMP_"ERROR i0"
 S ^VCOMP=^VCOMP_$T_" "
 e  S ^VCOMP=^VCOMP_"#"
 S ^VCOMP=^VCOMP_" "_$T,^VCORR="1 0 # 0" D ^VEXAMINE
 ;
25 W !,"II-25  set"
 S ^ABSN="20025",^ITEM="II-25  set",^NEXT="26^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP=""
 set ^VCOMP="A" set:1 ^VCOMP=^VCOMP_"B" set:000 ^VCOMP=^VCOMP_"ERROR set"
 S ^VCORR="AB" D ^VEXAMINE
 ;
26 W !,"II-26  s"
 S ^ABSN="20026",^ITEM="II-26  s",^NEXT="27^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP=""
 s ^VCOMP="C" s:1 ^VCOMP=^VCOMP_"D" s:000 ^VCOMP=^VCOMP_"ERROR s" S ^VCORR="CD" D ^VEXAMINE
 ;
27 W !,"II-27  kill"
 S ^ABSN="20027",^ITEM="II-27  kill",^NEXT="28^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="" ;(test corrected in V7.2;24/2/88)
 set ABCDEFGH=9,ABCDEFG1=10 s ABCDEFG2=11,ABCDEFG2(4)=1,ABCDEFG1(9)=1
 S ^VCOMP=ABCDEFGH_ABCDEFG1_ABCDEFG2 kill ABCDEFGH Kill ABCDEFG2
 S ^VCOMP=^VCOMP_$DATA(ABCDEFGH)_$DATA(ABCDEFG2)_$D(ABCDEFG1)
 S ^VCORR="910110011" D ^VEXAMINE
 ;
28 W !,"II-28  k"
 ;(test corrected in V7.2;24/2/88)
 S ^ABSN="20028",^ITEM="II-28  k",^NEXT="29^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="" KILL ABCDEFG1
 set ABCDEFGH=19,ABCDEFG1=110 s ABCDEFG2=111
 S ^VCOMP=ABCDEFGH_ABCDEFG1_ABCDEFG2
 k ABCDEFGH k:1 ABCDEFG2
 S ^VCOMP=^VCOMP_$DATA(ABCDEFGH)_$DATA(ABCDEFG2)_$D(ABCDEFG1)
 S ^VCORR="19110111001" D ^VEXAMINE
 ;
29 W !,"II-29  $data"
 S ^ABSN="20029",^ITEM="II-29  $data",^NEXT="30^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="" ;(test corrected in V7.2;24/2/88)
 SET ABCDEFGH=9,ABCDEFG1=10 s ABCDEFG2=11,ABCDEFG2(4)=1,ABCDEFG1(9)=1
 S ^VCOMP=ABCDEFGH_ABCDEFG1_ABCDEFG2 KILL ABCDEFGH K ABCDEFG2
 S ^VCOMP=^VCOMP_$data(ABCDEFGH)_$Data(ABCDEFG2)_$dATA(ABCDEFG1)
 S ^VCORR="910110011" D ^VEXAMINE
 ;
30 W !,"II-30  $d"
 ;(test corrected in V7.2;24/2/88)
 S ^ABSN="20030",^ITEM="II-30  $d",^NEXT="31^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="" KILL ABCDEFG1
 set ABCDEFGH=19,ABCDEFG1=110 s ABCDEFG2=111
 S ^VCOMP=ABCDEFGH_ABCDEFG1_ABCDEFG2
 K ABCDEFGH KILL ABCDEFG2
 S ^VCOMP=^VCOMP_$d(ABCDEFGH)_$d(ABCDEFG2)_$d(ABCDEFG1)
 S ^VCORR="19110111001" D ^VEXAMINE
 ;
31 W !,"II-31  xecute"
 S ^ABSN="20031",^ITEM="II-31  xecute",^NEXT="32^V2LCC2,V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP=""
 xecute "s AA=12,AB=20 set ^VCOMP=AA*AB" S ^VCORR="240" D ^VEXAMINE
 ;
32 W !,"II-32  x"
 S ^ABSN="20032",^ITEM="II-32  x",^NEXT="V2LCF1^VV2" D ^V2PRESET
 S ^VCOMP="",X="s BB=3,BA(2)=""ABCD"""
 x X S ^VCOMP=BB_BA(2) S ^VCORR="3ABCD" D ^VEXAMINE
 ;
END w !!,"End of 3---V2LCC2",!
 k  q
 Q
SUM S SUM=0 F I=1:1 S L=$T(+I) Q:L=""  F K=1:1:$L(L) S SUM=SUM+$A(L,K)
 Q
