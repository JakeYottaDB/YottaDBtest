V1BOC1	;BINARY OPERATORS -3.1- (&!_);YS-TS,V1BOC,VALIDATION VERSION 7.1;31-AUG-1987;
	;COPYRIGHT MUMPS SYSTEM LABORATORY 1978
	S PASS=0,FAIL=0
	W !!,"V1BOC1: TEST OF BINARY OPERATORS -3.1- (&)"
	W !!,"And (&)",!
140	W !,"I-140  expratoms are 0 or 1"
	S ITEM="I-140.1  1&1" S VCOMP=1&1,VCORR="1" D EXAMINER
	S ITEM="I-140.2  1&0" S VCOMP=1&0,VCORR="0" D EXAMINER
	S ITEM="I-140.3  0&1" S VCOMP=0&1,VCORR="0" D EXAMINER
	S ITEM="I-140.4  0&0" S VCOMP=0&0,VCORR="0" D EXAMINER
	;
141	W !,"I-141  expratoms are numlit"
	S ITEM="I-141.1  2&3" S VCOMP=2&3,VCORR="1" D EXAMINER
	S ITEM="I-141.2  4&0" S VCOMP=4&0,VCORR="0" D EXAMINER
	S ITEM="I-141.3  0&-5" S VCOMP=0&-5,VCORR="0" D EXAMINER
	S ITEM="I-141.4  0.06&-0.007" S VCOMP=0.06&-0.007,VCORR="1" D EXAMINER
	;
142	W !,"I-142  expratoms are strlit"
	S ITEM="I-142.1  ""A""&""B""" S VCOMP="A"&"B",VCORR="0" D EXAMINER
	S ITEM="I-142.2  ""-0.0A""&""2B""" S VCOMP="-0.0A"&"2B",VCORR="0" D EXAMINER
	;
143	W !,"I-143  expratoms are empty string"
	S ITEM="I-143  " S VCOMP=""&"" S C="0" D EXAMINER
	;
144	W !,"I-144  expratoms are lvn"
	S A=0,B="B",C=2,%D="3Z"
	S ITEM="I-144.1  A&B" S VCOMP=A&B,VCORR="0" D EXAMINER
	S ITEM="I-144.2  C&%D" S VCOMP=C&%D,VCORR="1" D EXAMINER
	;
	W !!,"Nand ('&)",!
145	W !,"I-145  expratoms are 0 or 1"
	S ITEM="I-145.1  1'&1" S VCOMP=1'&1,VCORR="0" D EXAMINER
	S ITEM="I-145.2  1'&0" S VCOMP=1'&0,VCORR="1" D EXAMINER
	S ITEM="I-145.3  0'&1" S VCOMP=0'&1,VCORR="1" D EXAMINER
	S ITEM="I-145.4  0'&0" S VCOMP=0'&0,VCORR="1" D EXAMINER
	;
146	W !,"I-146  expratoms are numlit"
	S ITEM="I-146.1  2'&30000" S VCOMP=2'&30000,VCORR="0" D EXAMINER
	S ITEM="I-146.2  40'&0" S VCOMP=40'&0,VCORR="1" D EXAMINER
	S ITEM="I-146.3  2E-10'&0" S VCOMP=2E-10'&0,VCORR="1" D EXAMINER
	S ITEM="I-146.4  00.0300'&4E10" S VCOMP=00.0300'&4E10,VCORR="0" D EXAMINER
	;
147	W !,"I-147  expratoms are strlit"
	S ITEM="I-147.1  ""A""'&""B""" S VCOMP="A"'&"B",VCORR="1" D EXAMINER
	S ITEM="I-147.2  ""-0.0A""'&""2B""" S VCOMP="-0.0A"'&"2B",VCORR="1" D EXAMINER
	;
148	W !,"I-148  expratoms are empty string"
	S ITEM="I-148  " S VCOMP=""'&"" S C="1" D EXAMINER
	;
149	W !,"I-149  expratoms are lvn"
	S %A=0,B="B",C=2,D="3Z"
	S ITEM="I-149.1  C'&D" S VCOMP=C'&D,VCORR="0" D EXAMINER
	S ITEM="I-149.2  D'&%A" S VCOMP=D'&%A,VCORR="1" D EXAMINER
	;
END	W !!,"END OF V1BOC1",!
	S ROUTINE="V1BOC1",TESTS=26,AUTO=26,VISUAL=0 D ^VREPORT
	K  Q
	;
EXAMINER	I VCORR=VCOMP S PASS=PASS+1 W !,"   PASS  ",ITEM W:$Y>55 # Q
	S FAIL=FAIL+1 W !,"** FAIL  ",ITEM W:$Y>55 #
	W !,"           COMPUTED =""",VCOMP,"""" W:$Y>55 #
	W !,"           CORRECT  =""",VCORR,"""" W:$Y>55 #
	Q
