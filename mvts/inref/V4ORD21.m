 K V(-192)
 S ^VCOMP=^VCOMP_$O(V(""),1)_" "
 K V(-1)
 S ^VCOMP=^VCOMP_$O(V(""),1)_" "
 K V(0)
 S ^VCOMP=^VCOMP_$O(V(""),1)_" "
 K V(839)
 S ^VCOMP=^VCOMP_$O(V(""),1)_" "
 K V("AAA")
 S ^VCOMP=^VCOMP_$O(V(""),1)_" "
 S ^VCORR="-192 -1 0 839 AAA  " D ^VEXAMINE
 ;
3 S ^ABSN="40683",^ITEM="IV-683  numeric interpretation of a subscript"
