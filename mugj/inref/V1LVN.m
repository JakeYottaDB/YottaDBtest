V1LVN	;ACCEPTABLE LOCAL VARIABLE NAME;YS-TS,,VALIDATION VERSION 7.1;31-AUG-1987;
	;COPYRIGHT MUMPS SYSTEM LABORATORY 1978
	S PASS=0,FAIL=0
	W !!,"V1LVN: TEST OF ACCEPTABLE LOCAL VARIABLE NAMES",!
611	W !,"I-611  lvn is ""%"""
	S ITEM="I-611  "
	S %="%" S VCOMP=% S VCORR="%" D EXAMINER
	;
612	W !,"I-612  lvn is ""%"" and alphas"
	S ITEM="I-612  "
	S %ABCDEF="%ABCDEF" S VCOMP=%ABCDEF S VCORR="%ABCDEF" D EXAMINER
	;
613	W !,"I-613  lvn is ""%"" and digits"
	S ITEM="I-613  "
	S %1234="%1234" S VCOMP=%1234 S VCORR="%1234" D EXAMINER
	;
614	W !,"I-614  lvn is ""%"" followed by a alpha and a digit"
	S ITEM="I-614  "
	S %A1="%A1" S VCOMP=%A1 S VCORR="%A1" D EXAMINER
	;
615	W !,"I-615  lvn is alpha"
	S ITEM="I-615.1  A"
	S A="A" S VCOMP=A S VCORR="A" D EXAMINER
	;
	S ITEM="I-615.2  AB"
	S AB="AB" S VCOMP=AB S VCORR="AB" D EXAMINER
	;
	S ITEM="I-615.3  ABCD"
	S ABCD="ABCD" S VCOMP=ABCD S VCORR="ABCD" D EXAMINER
	;
	S ITEM="I-615.4  ABCDEF"
	S ABCDEF="ABCDEF" S VCOMP=ABCDEF S VCORR="ABCDEF" D EXAMINER
	;
	S ITEM="I-615.5  ABCDEFG"
	S ABCDEFG="ABCDEFG" S VCOMP=ABCDEFG S VCORR="ABCDEFG" D EXAMINER
	;
616	W !,"I-616  lvn is combination of alpha and digit"
	S ITEM="I-616.1  Q00"
	S Q00="Q00" S VCOMP=Q00 S VCORR="Q00" D EXAMINER
	;
	S ITEM="I-616.2  Z1Y2X3"
	S Z1Y2X3="Z1Y2X3" S VCOMP=Z1Y2X3 S VCORR="Z1Y2X3" D EXAMINER
	;
	S ITEM="I-616.3  Q000000A"
	S Q000000A="Q000000A" S VCOMP=Q000000A S VCORR="Q000000A" D EXAMINER
	;
617	W !,"I-617  maximum length of lvn"
	S ITEM="I-617  %ABC456G"
	S %ABC456G="%ABC456G" S VCOMP=%ABC456G S VCORR="%ABC456G" D EXAMINER
	;
618	W !,"I-618  8 levels depth of subscript"
	S ITEM="I-618.1  1 level depth of subscript"
	S A(1)="A(1)" S VCOMP=A(1) S VCORR="A(1)" D EXAMINER
	;
	S ITEM="I-618.2  ABCDEFGH(1,2,3,4,5,6,7,8)"
	S ABCDEFGH(1,2,3,4,5,6,7,8)="ABCDEFGH(1,2,3,4,5,6,7,8)" S VCOMP=ABCDEFGH(1,2,3,4,5,6,7,8)
	S VCORR="ABCDEFGH(1,2,3,4,5,6,7,8)" D EXAMINER
	;
	S ITEM="I-618.3  %1X2Y3Z(1,10,100,12,123,2,01)"
	S %1X2Y3Z(1,10,100,12,123,2,01)="%1X2Y3Z(1,10,100,12,123,2,01)" S VCOMP=%1X2Y3Z(1,10,100,12,123,2,01)
	S VCORR="%1X2Y3Z(1,10,100,12,123,2,01)" D EXAMINER
	;
END	W !!,"END OF V1LVN",!
	S ROUTINE="V1LVN",TESTS=16,AUTO=16,VISUAL=0 D ^VREPORT
	K  Q
	;
EXAMINER	I VCORR=VCOMP S PASS=PASS+1 W !,"   PASS  ",ITEM W:$Y>55 # Q
	S FAIL=FAIL+1 W !,"** FAIL  ",ITEM W:$Y>55 #
	W !,"           COMPUTED =""",VCOMP,"""" W:$Y>55 #
	W !,"           CORRECT  =""",VCORR,"""" W:$Y>55 #
	Q
